export function createMockLlmResponse(userMessage, nodeTitle = '현재 노드') {
  return `## ${nodeTitle} 기준 답변

질문 내용은 **${userMessage}**이다. 현재 세션의 흐름을 유지하면서 핵심만 정리한다.

- 먼저 현재 노드의 목적과 직접 연결되는 내용만 답변한다.
- 추가로 파고들 질문은 AI 답변의 \`브랜치 생성\` 버튼으로 분기할 수 있다.
- main 경로를 지정하면 중심 흐름과 탐색 흐름을 분리해서 확인할 수 있다.

| 구분 | 처리 방식 |
| --- | --- |
| 본래 질문 | 현재 노드에서 계속 진행 |
| 꼬리질문 | 별도 브랜치로 분리 |
| 병합 필요 흐름 | 전체 그래프에서 노드 합치기 |

> 답변은 실제 API 연결 전까지 Mock LLM 응답으로 제공된다.`
}
